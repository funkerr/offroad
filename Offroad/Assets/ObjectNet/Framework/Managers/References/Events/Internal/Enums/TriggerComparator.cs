namespace com.onlineobject.objectnet {
    /// <summary>
    /// Defines the set of comparators that can be used to trigger certain conditions.
    /// </summary>
    public enum TriggerComparators {
        /// <summary>
        /// Represents equality comparison.
        /// </summary>
        EqualsTo,

        /// <summary>
        /// Represents inequality comparison.
        /// </summary>
        Different,

        /// <summary>
        /// Represents a comparison where the first value is greater than the second.
        /// </summary>
        Greather,

        /// <summary>
        /// Represents a comparison where the first value is less than the second.
        /// </summary>
        Less
    }

}