namespace com.onlineobject.objectnet {
    /// <summary>
    /// Represents an entry for a server address in a network configuration or directory.
    /// </summary>
    public struct ServerAddressEntry {
        /// <summary>
        /// The IP address or URL of the server.
        /// </summary>
        public string Address;

        /// <summary>
        /// The human-readable name or identifier for the server.
        /// </summary>
        public string Name;
    }

}